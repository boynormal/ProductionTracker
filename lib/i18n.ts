export type Locale = 'th' | 'en'
export type TranslationKey = keyof typeof translations.th

interface I18nContextValue {
  locale: Locale
  setLocale: (locale: Locale) => void
  t: (key: TranslationKey) => string
}

const I18nContext = createContext<I18nContextValue>({
  locale: 'th',
  setLocale: () => {},
  t: key => key,
})

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>('th')

  useEffect(() => {
    const stored = localStorage.getItem('locale') as Locale | null
    if (stored === 'en' || stored === 'th') setLocaleState(stored)
  }, [])

  const setLocale = useCallback((nextLocale: Locale) => {
    setLocaleState(nextLocale)
    if (typeof window !== 'undefined') localStorage.setItem('locale', nextLocale)
  }, [])

  const t = useCallback(
    (key: TranslationKey): string => translations[locale][key] ?? key,
    [locale],
  )

  return React.createElement(I18nContext.Provider, { value: { locale, setLocale, t } }, children)
}

export function useI18n() {
  return useContext(I18nContext)
}
